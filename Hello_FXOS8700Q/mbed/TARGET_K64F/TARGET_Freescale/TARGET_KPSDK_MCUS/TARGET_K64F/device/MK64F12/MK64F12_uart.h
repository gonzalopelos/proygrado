        uint8_t NF : 1;                //!< [2] Noise Flag
        uint8_t OR : 1;                //!< [3] Receiver Overrun Flag
        uint8_t IDLE : 1;              //!< [4] Idle Line Flag
        uint8_t RDRF : 1;              //!< [5] Receive Data Register Full Flag
